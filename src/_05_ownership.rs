#![allow(dead_code, unused_variables)]

pub fn run() {
    println!("=== Rust 所有权学习 ===\n");

    // -------------------------------------------------------------------------
    // 1. 所有权规则
    // -------------------------------------------------------------------------
    println!("【1. 所有权规则】\n");
    
    // 所有权三大规则：
    // 1. Rust 中每个值都有一个所有者（owner）
    // 2. 同一时刻只能有一个所有者
    // 3. 当所有者离开作用域，值将被丢弃

    // -------------------------------------------------------------------------
    // 2. 变量作用域
    // -------------------------------------------------------------------------
    println!("【2. 变量作用域】");
    
    {
        let s = "hello";  // s 从这里开始有效
        println!("作用域内: {}", s);
    }  // s 离开作用域，被丢弃
    // println!("{}", s);  // 错误！s 已不存在

    // -------------------------------------------------------------------------
    // 3. String 类型 - 堆分配的数据
    // -------------------------------------------------------------------------
    println!("\n【3. String 类型】");
    
    // 字符串字面量：不可变，存储在二进制文件中
    let s1: &str = "hello";
    
    // String 类型：可变，存储在堆上
    let mut s2 = String::from("hello");
    s2.push_str(", world!");  // 可以修改
    println!("{}", s2);
    
    // String 的内存布局：
    // - ptr: 指向堆上的数据
    // - len: 当前长度
    // - capacity: 总容量

    // -------------------------------------------------------------------------
    // 4. 移动 (Move) - 所有权的转移
    // -------------------------------------------------------------------------
    println!("\n【4. 移动语义】");
    
    let s1 = String::from("hello");
    let s2 = s1;  // s1 的所有权移动到 s2
    
    // println!("{}", s1);  // 错误！s1 已失效
    println!("s2: {}", s2);
    
    println!("\n为什么需要移动？");
    println!("如果复制所有数据会很慢");
    println!("如果两个指针指向同一数据，释放时会 double free");
    println!("Rust 的解决方案：移动所有权，使原变量失效");

    // -------------------------------------------------------------------------
    // 5. 克隆 (Clone)
    // -------------------------------------------------------------------------
    println!("\n【5. 克隆 - 深拷贝】");
    
    let s1 = String::from("hello");
    let s2 = s1.clone();  // 显式深拷贝
    
    println!("s1: {}, s2: {}", s1, s2);  // 两个都有效

    // -------------------------------------------------------------------------
    // 6. 栈上数据的复制 - Copy Trait
    // -------------------------------------------------------------------------
    println!("\n【6. 栈上数据的复制】");
    
    // 简单类型实现了 Copy trait，赋值时会复制而不是移动
    let x = 5;
    let y = x;  // 复制，不是移动
    
    println!("x = {}, y = {}", x, y);  // 两个都有效
    
    // 实现 Copy 的类型：
    // - 所有整数类型：u8, i32, usize 等
    // - 浮点类型：f32, f64
    // - 布尔类型：bool
    // - 字符类型：char
    // - 元组（如果所有元素都是 Copy）
    // - 不可变引用 &T

    // -------------------------------------------------------------------------
    // 7. 函数与所有权
    // -------------------------------------------------------------------------
    println!("\n【7. 函数与所有权】");
    
    let s = String::from("hello");
    takes_ownership(s);  // s 的所有权移动到函数
    // println!("{}", s);  // 错误！s 已失效
    
    let x = 5;
    makes_copy(x);  // x 是 Copy，所以复制一份
    println!("x 仍然有效: {}", x);  // x 仍然有效

    // -------------------------------------------------------------------------
    // 8. 返回值与所有权
    // -------------------------------------------------------------------------
    println!("\n【8. 返回值与所有权】");
    
    let s1 = gives_ownership();  // 函数将所有权移动给 s1
    println!("s1: {}", s1);
    
    let s2 = String::from("hello");
    let s3 = takes_and_gives_back(s2);  // s2 移动进函数，函数返回值移动给 s3
    // println!("{}", s2);  // 错误！s2 已失效
    println!("s3: {}", s3);

    // -------------------------------------------------------------------------
    // 9. 引用 (References) - 借用
    // -------------------------------------------------------------------------
    println!("\n【9. 引用 - 借用】");
    
    let s1 = String::from("hello");
    let len = calculate_length(&s1);  // 传递引用，不获取所有权
    println!("'{}' 的长度是 {}", s1, len);  // s1 仍然有效
    
    // 引用规则：
    // 1. 可以有多个不可变引用，或者一个可变引用
    // 2. 引用必须始终有效（不能有悬垂引用）

    // -------------------------------------------------------------------------
    // 10. 可变引用
    // -------------------------------------------------------------------------
    println!("\n【10. 可变引用】");
    
    let mut s = String::from("hello");
    change(&mut s);  // 传递可变引用
    println!("修改后: {}", s);
    
    // 可变引用的限制：
    // 在特定作用域中，对于同一数据，只能有一个可变引用
    
    let mut s = String::from("hello");
    {
        let r1 = &mut s;
        r1.push_str(" world");
    }  // r1 离开作用域
    
    let r2 = &mut s;  // 现在可以创建新的可变引用
    r2.push_str("!");
    println!("s: {}", s);

    // -------------------------------------------------------------------------
    // 11. 悬垂引用 - Rust 防止
    // -------------------------------------------------------------------------
    println!("\n【11. 悬垂引用】");
    
    let reference = no_dangle();  // Rust 确保不会返回悬垂引用
    println!("安全的引用: {}", reference);

    // -------------------------------------------------------------------------
    // 12. 引用规则总结
    // -------------------------------------------------------------------------
    println!("\n【12. 引用规则总结】");
    
    let mut s = String::from("hello");
    
    // 规则 1：多个不可变引用
    let r1 = &s;
    let r2 = &s;
    println!("r1: {}, r2: {}", r1, r2);
    // r1 和 r2 最后一次使用后，可以创建可变引用
    
    // 规则 2：不可变引用和可变引用不能同时存在
    let r3 = &mut s;
    r3.push_str(" world");
    println!("r3: {}", r3);

    // -------------------------------------------------------------------------
    // 13. 切片 (Slice)
    // -------------------------------------------------------------------------
    println!("\n【13. 切片类型】");
    
    let s = String::from("hello world");
    
    // 字符串切片
    let hello = &s[0..5];   // 或 &s[..5]
    let world = &s[6..11];  // 或 &s[6..]
    let full = &s[..];      // 整个字符串
    
    println!("切片: {}, {}, {}", hello, world, full);
    
    // 其他类型切片
    let arr = [1, 2, 3, 4, 5];
    let slice = &arr[1..3];
    println!("数组切片: {:?}", slice);

    // -------------------------------------------------------------------------
    // 14. 所有权实战示例
    // -------------------------------------------------------------------------
    println!("\n【14. 实战示例】");
    
    // 示例 1：查找第一个单词
    let s = String::from("hello world");
    let word = first_word(&s);
    println!("第一个单词: {}", word);
    
    // 示例 2：所有权转移后无法使用
    let s1 = String::from("hello");
    let s2 = s1;
    // println!("{}", s1);  // 错误！
    println!("s2: {}", s2);
    
    // 示例 3：正确使用引用
    let s1 = String::from("hello");
    let s2 = &s1;  // 借用
    println!("s1: {}, s2: {}", s1, s2);

    // -------------------------------------------------------------------------
    // 15. 生命周期简介
    // -------------------------------------------------------------------------
    println!("\n【15. 生命周期概念】");
    
    // 生命周期确保引用始终有效
    // 详见 13_lifetimes.rs
    
    let s1 = String::from("hello");
    let s2 = String::from("world");
    let longer = longest(&s1, &s2);
    println!("更长的字符串: {}", longer);
}

