/// 基础函数示例
fn basic_function() {
    println!("这是一个基础函数");
}

/// 带参数的函数
fn print_labeled_measurement(value: i32, unit_label: char) {
    println!("测量值: {}{}", value, unit_label);
}

/// 返回值函数 - 使用 return 关键字
fn add_five(x: i32) -> i32 {
    return x + 5;  // 显式返回
}

/// 返回值函数 - 最后一个表达式自动返回
fn another_add(x: i32, y: i32) -> i32 {
    x + y  // 注意：没有分号！这是一个表达式
}

/// 提前返回示例
fn check_number(x: i32) -> &'static str {
    if x < 10 {
        return "小于10";  // 提前返回
    }
    "大于等于10"  // 默认返回
}

/// 整数加法
fn add_integers(a: i32, b: i32) -> i32 {
    a + b
}

/// 浮点数加法
fn add_floats(a: f64, b: f64) -> f64 {
    a + b
}

/// 函数作为参数
fn apply_function(value: i32, f: fn(i32) -> i32) {
    println!("应用函数: f({}) = {}", value, f(value));
}

/// 递归 - 阶乘
fn factorial(n: u64) -> u64 {
    if n <= 1 {
        1
    } else {
        n * factorial(n - 1)
    }
}

/// 递归 - 斐波那契数列
fn fibonacci(n: u64) -> u64 {
    if n <= 1 {
        n
    } else {
        fibonacci(n - 1) + fibonacci(n - 2)
    }
}

/// 返回函数
fn create_adder(x: i32) -> fn(i32) -> i32 {
    // 这里只能返回函数指针，不能返回闭包
    // 闭包需要在堆上分配，类型不同
    add_five
}

/// 返回闭包
fn create_multiplier(factor: i32) -> Box<dyn Fn(i32) -> i32> {
    // 使用 Box<dyn Fn> 返回闭包
    Box::new(move |x| x * factor)
}

/// 矩形结构体
struct Rectangle {
    width: u32,
    height: u32,
}

impl Rectangle {
    /// 方法 - 计算面积
    fn area(&self) -> u32 {
        self.width * self.height
    }
    
    /// 方法 - 计算周长
    fn perimeter(&self) -> u32 {
        2 * (self.width + self.height)
    }
    
    /// 关联函数 - 创建正方形
    fn square(size: u32) -> Rectangle {
        Rectangle {
            width: size,
            height: size,
        }
    }
}

/// 发散函数示例
fn may_panic(x: i32) -> i32 {
    if x == 0 {
        panic!("值为 0！");  // panic! 永远不返回
    }
    x * 2
}

/// 常量函数 - 编译时计算
const fn compute_at_compile(x: i32) -> i32 {
    x * x * x
}
