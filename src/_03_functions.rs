#![allow(dead_code, unused_variables)]

pub fn run() {
    println!("=== Rust 函数学习 ===\n");

    // -------------------------------------------------------------------------
    // 1. 函数定义
    // -------------------------------------------------------------------------
    println!("【1. 函数定义】");
    
    // 函数使用 fn 关键字定义
    // 函数名使用 snake_case 命名风格
    basic_function();
    
    // -------------------------------------------------------------------------
    // 2. 函数参数
    // -------------------------------------------------------------------------
    println!("\n【2. 函数参数】");
    
    // 参数必须声明类型
    print_labeled_measurement(5, 'h');
    
    // -------------------------------------------------------------------------
    // 3. 带返回值的函数
    // -------------------------------------------------------------------------
    println!("\n【3. 带返回值的函数】");
    
    let result = add_five(5);
    println!("5 + 5 = {}", result);
    
    // 返回值是最后一个表达式的值（没有分号）
    let sum = another_add(10, 20);
    println!("10 + 20 = {}", sum);

    // -------------------------------------------------------------------------
    // 4. 语句 vs 表达式
    // -------------------------------------------------------------------------
    println!("\n【4. 语句 vs 表达式】");
    
    // 语句 (Statement)：执行操作但不返回值
    // 表达式 (Expression)：计算并返回值
    
    // 语句示例
    let x = 5;  // 这是一个语句
    
    // 表达式示例
    let y = {
        let x = 3;
        x + 1  // 这是一个表达式（没有分号），返回 4
    };
    println!("代码块返回值: {}", y);

    // -------------------------------------------------------------------------
    // 5. 提前返回
    // -------------------------------------------------------------------------
    println!("\n【5. 提前返回】");
    
    println!("检查数字 5: {}", check_number(5));
    println!("检查数字 15: {}", check_number(15));

    // -------------------------------------------------------------------------
    // 6. 函数重载（Rust 不支持）
    // -------------------------------------------------------------------------
    println!("\n【6. 函数重载】");
    
    // Rust 不支持函数重载
    // 可以通过不同函数名或使用泛型/trait 来实现类似功能
    println!("加法整数: {}", add_integers(3, 4));
    println!("加法浮点: {}", add_floats(3.0, 4.0));

    // -------------------------------------------------------------------------
    // 7. 可变参数
    // -------------------------------------------------------------------------
    println!("\n【7. 可变参数】");
    
    // 使用宏来实现可变参数效果
    // 标准库中的 println! 和 vec! 就是宏
    println!("多个参数: {} {} {}", 1, 2, 3);

    // -------------------------------------------------------------------------
    // 8. 函数指针
    // -------------------------------------------------------------------------
    println!("\n【8. 函数指针】");
    
    // 函数可以作为值传递
    let fn_ptr: fn(i32) -> i32 = add_five;
    let result = fn_ptr(10);
    println!("函数指针调用: {}", result);
    
    // 函数作为参数
    apply_function(5, add_five);

    // -------------------------------------------------------------------------
    // 9. 闭包 (Closure)
    // -------------------------------------------------------------------------
    println!("\n【9. 闭包】");
    
    // 闭包是可以捕获环境的匿名函数
    // 语法: |参数| { 函数体 }
    
    let add_one = |x: i32| -> i32 { x + 1 };
    println!("闭包调用: {}", add_one(5));
    
    // 简化写法
    let add_two = |x| x + 2;
    println!("简化闭包: {}", add_two(5));
    
    // 捕获环境变量
    let multiplier = 10;
    let multiply = |x| x * multiplier;
    println!("捕获环境变量: {}", multiply(5));
    
    // 可变闭包
    let mut counter = 0;
    let mut increment = || {
        counter += 1;
        counter
    };
    println!("闭包递增: {}", increment());
    println!("闭包递增: {}", increment());

    // -------------------------------------------------------------------------
    // 10. 递归函数
    // -------------------------------------------------------------------------
    println!("\n【10. 递归函数】");
    
    let factorial_5 = factorial(5);
    println!("5! = {}", factorial_5);
    
    let fib_10 = fibonacci(10);
    println!("斐波那契第10项 = {}", fib_10);

    // -------------------------------------------------------------------------
    // 11. 高阶函数
    // -------------------------------------------------------------------------
    println!("\n【11. 高阶函数】");
    
    // 函数可以返回函数
    let add_func = create_adder(10);
    println!("返回函数的调用: {}", add_func(5));
    
    // 闭包作为返回值
    let multiplier_closure = create_multiplier(3);
    println!("返回闭包的调用: {}", multiplier_closure(5));

    // -------------------------------------------------------------------------
    // 12. 方法 (Methods)
    // -------------------------------------------------------------------------
    println!("\n【12. 方法】");
    
    // 方法定义在 impl 块中（详见结构体章节）
    let rect = Rectangle { width: 10, height: 20 };
    println!("矩形面积: {}", rect.area());
    println!("矩形周长: {}", rect.perimeter());

    // 关联函数（类似静态方法）
    let square = Rectangle::square(5);
    println!("正方形面积: {}", square.area());

    // -------------------------------------------------------------------------
    // 13. 发散函数 (Diverging Functions)
    // -------------------------------------------------------------------------
    println!("\n【13. 发散函数】");
    
    // 发散函数永远不会返回，返回类型是 !
    let result = may_panic(5);
    println!("没有 panic: {}", result);
    
    // let _ = may_panic(0);  // 这会 panic!

    // -------------------------------------------------------------------------
    // 14. const 泛型与常量函数
    // -------------------------------------------------------------------------
    println!("\n【14. 常量函数】");
    
    // const 函数可以在编译时计算
    const COMPILE_TIME_VALUE: i32 = compute_at_compile(5);
    println!("编译时计算: {}", COMPILE_TIME_VALUE);
}

// ============================================================================
// 函数定义区域
