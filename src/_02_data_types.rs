// ============================================================================
// 02_data_types.rs - Rust 数据类型详解
// ============================================================================

pub fn run() {
    println!("=== Rust 数据类型学习 ===\n");

    // -------------------------------------------------------------------------
    // 1. 标量类型 (Scalar Types)
    // -------------------------------------------------------------------------
    println!("【1. 标量类型】\n");

    // ----------------------
    // 1.1 整数类型
    // ----------------------
    println!("--- 1.1 整数类型 ---");
    
    // 有符号整数 (i): 可存储正负数
    let i8_val: i8 = -128;        // -128 到 127
    let i16_val: i16 = -32768;    // -32768 到 32767
    let i32_val: i32 = -100;      // 默认整数类型
    let i64_val: i64 = -1000;
    let i128_val: i128 = -10000;
    let isize_val: isize = -10;   // 指针大小，64位系统是64位
    
    // 无符号整数 (u): 只能存储非负数
    let u8_val: u8 = 255;         // 0 到 255
    let u16_val: u16 = 65535;
    let u32_val: u32 = 100;
    let u64_val: u64 = 1000;
    let u128_val: u128 = 10000;
    let usize_val: usize = 10;    // 指针大小，64位系统是64位
    
    println!("i8: {}, i32: {}", i8_val, i32_val);
    println!("u8: {}, u32: {}", u8_val, u32_val);

    // 整数字面量表示法
    let decimal = 98_222;         // 十进制，下划线提高可读性
    let hex = 0xff;               // 十六进制
    let octal = 0o77;             // 八进制
    let binary = 0b1111_0000;     // 二进制
    let byte = b'A';              // 字节（仅限 u8）
    
    println!("十进制: {}, 十六进制: {}, 八进制: {}, 二进制: {}, 字节: {}", 
             decimal, hex, octal, binary, byte);

    // ----------------------
    // 1.2 浮点类型
    // ----------------------
    println!("\n--- 1.2 浮点类型 ---");
    
    let x = 2.0;      // f64，默认浮点类型
    let y: f32 = 3.0; // f32，单精度
    
    // f64 vs f32：f64 精度更高，现代CPU上速度相当，推荐使用
    println!("f64: {}, f32: {}", x, y);
    
    // 浮点运算
    let sum = x + 5.0;
    let product = x * 3.5;
    let quotient = 10.0 / 3.0;
    println!("和: {}, 积: {}, 商: {}", sum, product, quotient);

    // ----------------------
    // 1.3 布尔类型
    // ----------------------
    println!("\n--- 1.3 布尔类型 ---");
    
    let t = true;
    let f: bool = false;  // 显式类型注解
    println!("true: {}, false: {}", t, f);

    // ----------------------
    // 1.4 字符类型
    // ----------------------
    println!("\n--- 1.4 字符类型 ---");
    
    let c = 'z';
    let z: char = 'ℤ';
    let heart_eyed_cat = '😻';
    let chinese = '中';
    let emoji = '🦀';
    
    // char 是 4 字节，代表一个 Unicode 标量值
    println!("字符: {} {} {} {} {}", c, z, heart_eyed_cat, chinese, emoji);

    // -------------------------------------------------------------------------
    // 2. 复合类型 (Compound Types)
    // -------------------------------------------------------------------------
    println!("\n【2. 复合类型】\n");

    // ----------------------
    // 2.1 元组 (Tuple)
    // ----------------------
    println!("--- 2.1 元组 ---");
    
    let tup: (i32, f64, u8) = (500, 6.4, 1);
    println!("元组: {:?}", tup);
    
    // 解构元组
    let (x, y, z) = tup;
    println!("解构后: x = {}, y = {}, z = {}", x, y, z);
    
    // 通过索引访问
    let five_hundred = tup.0;
    let six_point_four = tup.1;
    let one = tup.2;
    println!("索引访问: {}, {}, {}", five_hundred, six_point_four, one);
    
    // 单元素元组
    let single = (5,);  // 注意逗号
    let not_a_tuple = (5);  // 这只是一个括号内的整数！
    println!("单元素元组: {:?}", single);

    // ----------------------
    // 2.2 数组 (Array)
    // ----------------------
    println!("\n--- 2.2 数组 ---");
    
    // 数组：固定长度，存储在栈上
    let arr = [1, 2, 3, 4, 5];
    println!("数组: {:?}", arr);
    
    // 指定类型和长度
    let arr2: [i32; 5] = [1, 2, 3, 4, 5];
    
    // 重复初始化
    let arr3 = [3; 5];  // 等同于 [3, 3, 3, 3, 3]
    println!("重复初始化: {:?}", arr3);
    
    // 访问元素
    let first = arr[0];
    let second = arr[1];
    println!("第一个元素: {}, 第二个元素: {}", first, second);
    
    // 数组越界会在运行时 panic
    // let error = arr[10];  // 运行时错误！
    
    // 数组切片
    let slice = &arr[1..3];
    println!("切片 [1..3]: {:?}", slice);

    // -------------------------------------------------------------------------
    // 3. 类型推断
    // -------------------------------------------------------------------------
    println!("\n【3. 类型推断】");
    
    // Rust 编译器会自动推断类型
    let inferred = 10;       // 推断为 i32
    let inferred_float = 3.0; // 推断为 f64
    
    // 根据后续使用推断类型
    let mut vec = Vec::new();
    vec.push(1);  // 现在编译器知道是 Vec<i32>
    println!("推断类型的 Vec: {:?}", vec);

    // -------------------------------------------------------------------------
    // 4. 类型别名
    // -------------------------------------------------------------------------
    println!("\n【4. 类型别名】");
    
    type Kilometers = i32;  // 创建类型别名
    
    let distance: Kilometers = 100;
    println!("距离: {} 公里", distance);
    
    // Kilometers 和 i32 是完全相同的类型

    // -------------------------------------------------------------------------
    // 5. 类型转换
    // -------------------------------------------------------------------------
    println!("\n【5. 类型转换】");
    
    // 使用 as 关键字进行显式转换
    let decimal = 65.4321;
    let integer = decimal as i32;  // 截断小数部分
    println!("浮点转整数: {} -> {}", decimal, integer);
    
    let integer = 65u8;  // 使用 u8 类型
    let character = integer as char;  // u8 可以直接转换为 char
    println!("整数转字符: {} -> {}", integer, character);
    
    // 或者使用 char::from_u32
    let unicode_val: u32 = 0x1F600;  // 😀 emoji
    if let Some(emoji) = char::from_u32(unicode_val) {
        println!("Unicode 转 char: {} -> {}", unicode_val, emoji);
    }
    
    // 注意：溢出行为
    let large: i32 = 300;
    let small: i8 = large as i8;  // 溢出！
    println!("溢出转换: {} -> {}", large, small);  // 结果：44

    // -------------------------------------------------------------------------
    // 6. 空类型
    // -------------------------------------------------------------------------
    println!("\n【6. 空类型】");
    
    // unit 类型：空元组，表示没有有意义的值
    let unit = ();
    println!("unit 类型: {:?}", unit);
    
    // 函数如果没有返回值，返回 unit 类型
    fn no_return() -> () {
        println!("这个函数返回 unit");
    }
    no_return();

    // never 类型：! 表示永远不返回
    // 例如：panic!, continue, break 等
    // fn always_panic() -> ! {
    //     panic!("永远不返回");
    // }
}
