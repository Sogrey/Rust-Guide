// ============================================================================
// 01_variables.rs - Rust 变量详解
// ============================================================================

#![allow(dead_code, unused_variables)]

pub fn run() {
    println!("=== Rust 变量学习 ===\n");

    // -------------------------------------------------------------------------
    // 1. 变量声明 - 使用 let 关键字
    // -------------------------------------------------------------------------
    println!("【1. 变量声明】");
    
    // Rust 默认变量是不可变的 (immutable)
    let x = 5;
    println!("x = {}", x);
    // x = 6;  // 错误！不能对不可变变量重新赋值

    // -------------------------------------------------------------------------
    // 2. 可变变量 - 使用 mut 关键字
    // -------------------------------------------------------------------------
    println!("\n【2. 可变变量】");
    
    let mut y = 5;
    println!("y 初始值: {}", y);
    y = 10;  // 可变变量可以重新赋值
    println!("y 修改后: {}", y);

    // -------------------------------------------------------------------------
    // 3. 变量遮蔽 (Variable Shadowing)
    // -------------------------------------------------------------------------
    println!("\n【3. 变量遮蔽】");
    
    let spaces = "   ";       // 字符串类型
    println!("spaces 是字符串: '{}'", spaces);
    
    let spaces = spaces.len(); // 新变量遮蔽了旧变量，类型可以改变！
    println!("spaces 现在是数字: {}", spaces);

    // 遮蔽 vs mut 的区别：
    // - 遮蔽：创建新变量，可以改变类型
    // - mut：修改现有变量，类型不能改变

    // -------------------------------------------------------------------------
    // 4. 常量 (Constants)
    // -------------------------------------------------------------------------
    println!("\n【4. 常量】");
    
    // 常量使用 const 关键字，必须注明类型
    // 常量名约定使用 SCREAMING_SNAKE_CASE
    // 常量必须在编译时就能确定值
    const MAX_POINTS: u32 = 100_000;  // 下划线提高可读性
    println!("最大分数: {}", MAX_POINTS);

    // 常量 vs 变量：
    // - 常量：永远不可变，必须注明类型，可在任何作用域声明
    // - 变量：默认不可变，可以用 mut，类型可推断

    // -------------------------------------------------------------------------
    // 5. 类型标注
    // -------------------------------------------------------------------------
    println!("\n【5. 类型标注】");
    
    let z: i32 = 10;  // 显式标注类型
    let pi: f64 = 3.14159;
    let is_rust_fun: bool = true;
    let letter: char = '🦀';
    
    println!("整数 z: {}", z);
    println!("浮点数 pi: {}", pi);
    println!("布尔值 is_rust_fun: {}", is_rust_fun);
    println!("字符 letter: {}", letter);

    // -------------------------------------------------------------------------
    // 6. 变量作用域
    // -------------------------------------------------------------------------
    println!("\n【6. 变量作用域】");
    
    let outer_var = "外部变量";
    
    {
        let inner_var = "内部变量";
        println!("内部作用域可以访问: {}", outer_var);
        println!("内部作用域可以访问: {}", inner_var);
    }
    
    println!("外部作用域可以访问: {}", outer_var);
    // println!("{}", inner_var);  // 错误！内部变量已离开作用域

    // -------------------------------------------------------------------------
    // 7. 未使用的变量警告
    // -------------------------------------------------------------------------
    println!("\n【7. 未使用的变量】");
    
    // 使用下划线前缀来消除未使用变量的警告
    let _unused_var = 42;
    
    // 下划线本身是一个特殊变量，会丢弃任何值
    let _ = "这个值会被丢弃";

    // -------------------------------------------------------------------------
    // 8. 解构赋值
    // -------------------------------------------------------------------------
    println!("\n【8. 解构赋值】");
    
    let (a, b, c) = (1, 2, 3);
    println!("a = {}, b = {}, c = {}", a, b, c);
    
    let array = [10, 20, 30];
    let [first, ..] = array;
    println!("数组第一个元素: {}", first);

    // -------------------------------------------------------------------------
    // 9. 变量命名规则
    // -------------------------------------------------------------------------
    println!("\n【9. 变量命名规则】");
    
    // 有效命名：
    let snake_case = "蛇形命名（推荐）";
    let _starts_with_underscore = "下划线开头";
    let _has_number_123 = "可以包含数字";
    
    // 无效命名：
    // let 1starts_with_number = 1;  // 错误！不能以数字开头
    // let has-dash = 1;              // 错误！不能包含连字符
    // let let = 1;                   // 错误！不能使用关键字
    
    println!("推荐使用 snake_case 命名风格");
}
